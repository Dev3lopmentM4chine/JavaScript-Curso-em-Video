var num = 10
num **= 4 //'n' recebe o valor de 'n' elevado a 4
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)
num *= 5 //'n' recebe o valor de 'n' vezes 5
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)
num /= 4 //'n' recebe o valor de 'n' dividido por 4
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)
num %= 2 //'n' recebe o valor do resto da divisão de 'n' por 2 
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)
num += 2 //'n' recebe o valor de 'n' mais 2    
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)
num -= 5 //'n' recebe o valor de 'n' menos 5
console.log(`A variavel 'num' que vale ${num.toLocaleString('pt-BR', {style: 'currency', currency: 'BRL'})}`)

//Pré incremento 
++num
--num

//Pós incremento
num++
